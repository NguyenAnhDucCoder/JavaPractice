package Entity;

public class Share {
    private String icon;
    private String link;

    public Share() {
    }

    public Share(String icon, String link) {
        this.icon = icon;
        this.link = link;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
    
}
